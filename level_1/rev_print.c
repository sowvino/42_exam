#include <unistd.h>

int main(int argv,char ** argc)
{
    int i;
    if(argv==2)
    {
        i =0;
        while(argc[1][i])
            i++;
        i--;
        while(i >= 0)
        {
            write(1,&argc[1][i],1);
            i--;
        }
    }
    write(1,"\n",1);
    return (0);
}