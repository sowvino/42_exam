#include <stdio.h>

void	ft_swap(int *a, int *b)
{
    int c;
    c = *a;
    *a = *b;
    *b = c;
}
int main()
{
    int *a ;
    int *b;
    int c ;
    int d;
    c = 3;
    d = 5;
    a = &c;
    b = &d;
    ft_swap(a,b);
    printf("%d %d", *a ,*b);
}